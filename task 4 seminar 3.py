def print_triangle(size, symb):
    """
    Выводит треугольник, сначала увеличивающийся, затем уменьшающийся,
    используя заданный символ.

    Args:
        size: Количество строк в треугольнике (высота до середины).
        symb: Символ, которым заполняется  треугольник.
    """
    # Цикл для верхней половины треугольника
    for i in range(1, size + 1):
        print(symb * i)  # Выводим строку из символов

    # Цикл для нижней половины треугольника (исключаем центральную строку)
    for i in range(size - 1, 0, -1):
        print(symb * i)  # Выводим строку из символов

# Пример использования функции:
size = int(input("Введите размер треугольника: "))
symb = input("Введите символ для заполнения: ")

print_triangle(size, symb)